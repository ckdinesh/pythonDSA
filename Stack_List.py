#
#   Stack is LIFO(Last In First Out)
#   Using append and pop functions

stack_using_list = []
#Adding elements to list at the end
stack_using_list.append(12)
stack_using_list.append(34)
stack_using_list.append(56)
stack_using_list.append(78)
stack_using_list.append(910)
print(stack_using_list)
# Remove elements from end of the list
stack_using_list.pop()
print(stack_using_list)
stack_using_list.pop()
print(stack_using_list)
stack_using_list.pop()
print(stack_using_list)